    private void btnCekStrukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCekStrukMouseClicked
        // TODO add your handling code here:
        if(txtNama.getText().equals("") || txtAlamat.getText().equals("") || txtNoHp.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Data Pemesan Harus Diisi Lengkap!!!");
        }
        else if(total == 0){
            JOptionPane.showMessageDialog(rootPane, "Tidak Ada Menu yang Dipilih.");
        }
        else{
            printStruk += "\n===============================================" + 
                    "\n Nama\t: " + txtNama.getText() +
                    "\n Alamat\t: " + txtAlamat.getText() +
                    "\n No. HP\t: " + txtNoHp.getText() +
                    "\n===============================================" +
                    "\n Daftar Pesanan:\n";
            if(cbSteak.isSelected()){
                printStruk += "\n - Steak\t\t\t\t(50000)";
            }
            if(cbSpagetti.isSelected()){
                printStruk += "\n - Spagetti\t\t\t\t(40000)";
            }
            if(cbPizza.isSelected()){
                printStruk += "\n - Pizza\t\t\t\t(80000)";
            }
            
            printStruk += "\n\n TOTAL\t\t\t\t\t" + txtTotal.getText() +
                    "\n===============================================\n";
            
            
            txtStruk.setText(printStruk);
            clearForm();
        }
    }//GEN-LAST:event_btnCekStrukMouseClicked

    private void btnSimpanStrukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanStrukMouseClicked
        // TODO add your handling code here:
        String s = replaceSeparator(printStruk);
        
        if(printStruk != ""){
            try{
                FileWriter f = new FileWriter(fileName);
                // \n nya masih gakebaca
                f.write(s);
//                f.write(txtStruk.getText());
                f.close();
                
                JOptionPane.showMessageDialog(rootPane, "Struk Berhasil Disimpan!");
                txtStruk.setText("");
                printStruk = "";
            } catch (IOException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSimpanStrukMouseClicked

