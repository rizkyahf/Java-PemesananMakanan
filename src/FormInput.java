    private void clearForm(){
        txtNama.setText("");
        txtAlamat.setText("");
        txtNoHp.setText("");
        cbSteak.setSelected(false);
        cbSpagetti.setSelected(false);
        cbPizza.setSelected(false);
        
        total = 0;
        txtTotal.setText(""+total);
    }
    
    private String replaceSeparator(String s){
        String separator = System.lineSeparator();
        return s.replace("\n", separator);
    }
    
